public class objetoArista {
    private String caracter;
    private int nodoInicio;
    private int nodoFin;
    /**
     * @return the caracter
     */
    public String getCaracter() {
        return caracter;
    }

    /**
     * @param caracter the caracter to set
     */
    public void setCaracter(String caracter) {
        this.caracter = caracter;
    }

    /**
     * @return the nodoInicio
     */
    public int getNodoInicio() {
        return nodoInicio;
    }

    /**
     * @param nodoInicio the nodoInicio to set
     */
    public void setNodoInicio(int nodoInicio) {
        this.nodoInicio = nodoInicio;
    }

    /**
     * @return the nodoFin
     */
    public int getNodoFin() {
        return nodoFin;
    }

    /**
     * @param nodoFin the nodoFin to set
     */
    public void setNodoFin(int nodoFin) {
        this.nodoFin = nodoFin;
    }
    
}
